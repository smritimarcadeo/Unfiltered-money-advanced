export const getByCategory = (cat) => PRODUCTS.filter((p) => p.category === cat);
export const getById = (id) => PRODUCTS.find((p) => p.id === id);
export const getFeatured = (cat) => getByCategory(cat).filter((p) => p.featured);
export const getCategory = (slug) => CATEGORIES.find((c) => c.slug === slug);

// Products in the same category that share the most tags — used for "Similar options".
export const getSimilar = (product, limit = 3) =>
  PRODUCTS.filter((p) => p.category === product.category && p.id !== product.id)
    .map((p) => ({ p, overlap: p.tags.filter((t) => product.tags.includes(t)).length }))
    .sort((a, b) => b.overlap - a.overlap || b.p.rating - a.p.rating)
    .slice(0, limit)
    .map((x) => x.p);
